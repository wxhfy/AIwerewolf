def insert_entries(conn, entries, doc_type):
    """Insert strategy entries into the database."""
    c = conn.cursor()
    inserted = 0
    skipped = 0

    for entry in entries:
        doc_id = f"v2-{doc_type}-{uuid.uuid4().hex[:8]}"
        try:
            c.execute(
                """
                INSERT INTO strategy_knowledge_docs
                    (id, doc_type, role, phase, persona_scope,
                     situation_pattern, trigger_conditions,
                     recommended_action, avoid_action,
                     rationale, quality_score, confidence,
                     status, tags)
                VALUES (%s, %s, %s, %s, %s,
                        %s, %s,
                        %s, %s,
                        %s, %s, %s,
                        'active', %s)
                ON CONFLICT (id) DO NOTHING
            """,
                (
                    doc_id,
                    doc_type,
                    entry.get("role", "global"),
                    entry.get("phase", "global"),
                    entry.get("scope", "any"),
                    entry.get("situation", ""),
                    json.dumps(entry.get("triggers", []), ensure_ascii=False),
                    entry.get("recommended", ""),
                    entry.get("avoid", ""),
                    entry.get("rationale", ""),
                    entry.get("quality", 0.85),
                    entry.get("quality", 0.85),
                    json.dumps(entry.get("tags", []), ensure_ascii=False),
                ),
            )
            if c.rowcount > 0:
                inserted += 1
            else:
                skipped += 1
        except Exception as e:
            print(f"  ⚠️ Error inserting {doc_id}: {e}")
            skipped += 1

    conn.commit()
    return inserted, skipped


def print_summary(conn):
    """Print current strategy distribution."""
    c = conn.cursor()
    c.execute("""
        SELECT doc_type, role, COUNT(*) as cnt
        FROM strategy_knowledge_docs
        WHERE status='active'
        GROUP BY doc_type, role
        ORDER BY doc_type, cnt DESC
    """)
    print("\n=== Strategy Distribution ===")
    current_doc_type = ""
    for doc_type, role, cnt in c.fetchall():
        if doc_type != current_doc_type:
            current_doc_type = doc_type
            print(f"\n  [{doc_type}]")
        print(f"    {role:20s}: {cnt:4d}")

    c.execute("SELECT COUNT(*) FROM strategy_knowledge_docs WHERE status='active'")
    total = c.fetchone()[0]
    print(f"\n  Total active: {total}")


