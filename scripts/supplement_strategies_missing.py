def insert_all(conn, entries, doc_type):
    c = conn.cursor()
    ins = 0
    for entry in entries:
        did = f"supp-{doc_type}-{uuid.uuid4().hex[:8]}"
        try:
            c.execute(
                """
                INSERT INTO strategy_knowledge_docs
                    (id, doc_type, role, phase, situation_pattern, recommended_action,
                     avoid_action, quality_score, confidence, status, tags)
                VALUES (%s, %s, %s, %s, %s, %s, %s, %s, %s, 'active', %s)
                ON CONFLICT (id) DO NOTHING
            """,
                (
                    did,
                    doc_type,
                    entry["role"],
                    entry.get("phase", "global"),
                    entry.get("situation", ""),
                    entry.get("recommended", ""),
                    entry.get("avoid", ""),
                    entry.get("quality", 0.85),
                    entry.get("quality", 0.85),
                    json.dumps([entry["role"], entry.get("phase", "global")], ensure_ascii=False),
                ),
            )
            ins += c.rowcount
        except Exception as e:
            print(f"  Error: {e}")
    conn.commit()
    c.close()
    return ins


